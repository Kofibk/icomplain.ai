// Helper functions for search
export function searchProviders(query: string, type: 'bank' | 'credit' | 'car' | 'loan' | 'mortgage' | 'insurance' | 'pension' | 'all'): string[] {
  if (!query || query.length < 1) return []
  const q = query.toLowerCase()
  
  let lists: string[][] = []
  switch (type) {
    case 'bank': lists = [UK_BANKS]; break
    case 'credit': lists = [UK_CREDIT_CARD_PROVIDERS]; break
    case 'car': lists = [UK_CAR_FINANCE_LENDERS]; break
    case 'loan': lists = [UK_LOAN_PROVIDERS]; break
    case 'mortgage': lists = [UK_MORTGAGE_LENDERS]; break
    case 'insurance': lists = [UK_INSURANCE_PROVIDERS]; break
    case 'pension': lists = [UK_PENSION_PROVIDERS]; break
    case 'all': 
      lists = [UK_BANKS, UK_CREDIT_CARD_PROVIDERS, UK_CAR_FINANCE_LENDERS, UK_LOAN_PROVIDERS, UK_MORTGAGE_LENDERS, UK_INSURANCE_PROVIDERS, UK_PENSION_PROVIDERS]
      break
  }
  
  const combined = Array.from(new Set(lists.flat()))
  return combined.filter(name => name.toLowerCase().includes(q)).slice(0, 10)
}

export function searchCarMakes(query: string): string[] {
  if (!query || query.length < 1) return []
  const q = query.toLowerCase()
  return CAR_MANUFACTURERS.filter(make => make.toLowerCase().includes(q)).slice(0, 8)
}

export function getIssuesForCategory(category: string): string[] {
  return COMPLAINT_CATEGORIES[category as keyof typeof COMPLAINT_CATEGORIES]?.issues || []
}

export function getAllCategories(): { name: string; icon: string }[] {
  return Object.entries(COMPLAINT_CATEGORIES).map(([name, data]) => ({
    name,
    icon: data.icon,
  }))
}
